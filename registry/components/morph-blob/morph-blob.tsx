export default function MorphBlob({
  className = '',
  size = 300,
  color = '#8B5CF6',
  gradientColors,
  gradientDirection = 'diagonal',
  duration = 8,
  blur = 0,
  glow = false,
  glowIntensity = 20,
  variant = 'organic',
}: MorphBlobProps) {
  const paths = blobPaths[variant];
  const gradientId = `gradient-${Math.random().toString(36).substr(2, 9)}`;

  const getGradientCoords = () => {
    switch (gradientDirection) {
      case 'horizontal':
        return { x1: '0%', y1: '50%', x2: '100%', y2: '50%' };
      case 'vertical':
        return { x1: '50%', y1: '0%', x2: '50%', y2: '100%' };
      case 'diagonal':
      default:
        return { x1: '0%', y1: '0%', x2: '100%', y2: '100%' };
    }
  };

  const gradientCoords = getGradientCoords();
  const fillColor = gradientColors ? `url(#${gradientId})` : color;

  return (
    <div
      className={`relative ${className}`}
      style={{ width: size, height: size }}
    >
      {glow && (
        <div
          className="absolute inset-0"
          style={{
            background: gradientColors ? gradientColors[0] : color,
            filter: `blur(${glowIntensity}px)`,
            opacity: 0.4,
            borderRadius: '50%',
            transform: 'scale(0.8)',
          }}
        />
      )}
      <svg
        viewBox="-100 -100 200 200"
        width={size}
        height={size}
        style={{
          filter: blur > 0 ? `blur(${blur}px)` : undefined,
        }}
      >
        {gradientColors && (
          <defs>
            <linearGradient id={gradientId} {...gradientCoords}>
              <stop offset="0%" stopColor={gradientColors[0]} />
              <stop offset="100%" stopColor={gradientColors[1]} />
            </linearGradient>
          </defs>
        )}
        <motion.path
          fill={fillColor}
          initial={{ d: paths[0] }}
          animate={{
            d: paths,
          }}
          transition={{
            duration,
            repeat: Infinity,
            repeatType: 'mirror',
            ease: 'easeInOut',
          }}
        />
      </svg>
    </div>
  );
}
